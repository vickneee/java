public class Main {

    public static void main(String[] args) {

        // Constants are fixed values that do not change
        final int EXAMPLE = 4;

        // Print the value of the constant
        System.out.println(EXAMPLE);

        // EXAMPLE = 5; // This will cause a compilation error because EXAMPLE is a final variable

        // Constants are typically declared in uppercase letters
        // and are used to represent fixed values that do not change
        // Constants are declared using the final keyword
        // Constants are usually declared at the class level

        // Example of a constant
        final int MAX_USERS = 100;

        System.out.println(MAX_USERS);

    }
}
